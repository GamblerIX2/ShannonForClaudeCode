#!/usr/bin/env bash
# build-scan-config.sh — generate a Shannon YAML config from agent answers.
#
# Reads exclude paths (one per line) from stdin and writes a YAML file that
# Shannon's `-c <path>` flag accepts. Encodes:
#   - rules.avoid[].type=code_path entries from stdin
#   - exploit: true|false
#
# Usage: build-scan-config.sh <out-yaml-path> <exploit:true|false>
#
# Stdin: one exclude path per line. Blank lines and lines starting with '#'
#        are skipped. Paths may be either bare directory names ('node_modules')
#        or globs ('**/*.min.js') — both are valid for Shannon's code_path
#        rule type.

set -uo pipefail

out="${1:-}"
exploit="${2:-true}"

if [ -z "$out" ]; then
  echo "usage: $0 <out-yaml-path> <true|false>" >&2
  exit 2
fi

case "$exploit" in
  true|false) ;;
  *) echo "ERROR: exploit must be 'true' or 'false', got '$exploit'" >&2; exit 2 ;;
esac

# Collect excludes from stdin into an array, deduped, trimmed.
declare -a excludes=()
while IFS= read -r line || [ -n "$line" ]; do
  trimmed="$(printf '%s' "$line" | sed -E 's/^[[:space:]]+//; s/[[:space:]]+$//')"
  [ -z "$trimmed" ] && continue
  case "$trimmed" in
    \#*) continue ;;
  esac
  # Dedup — linear is fine, list is tiny.
  dup=false
  for e in "${excludes[@]:-}"; do
    if [ "$e" = "$trimmed" ]; then dup=true; break; fi
  done
  $dup || excludes+=("$trimmed")
done

# YAML emission. We always emit `exploit` and (when there are excludes)
# `rules.avoid` — both are top-level keys per Shannon's config-schema.json.
# Values are quoted with single quotes to avoid YAML interpreting glob chars.
tmp="$(mktemp)"
{
  echo "# Auto-generated by shannon-for-claude-code plugin."
  echo "# Edit by re-running the plugin; manual edits will be overwritten."
  echo
  # Schema requires string "true"/"false" (not YAML boolean), so quote it.
  printf "exploit: '%s'\n" "$exploit"
  if [ "${#excludes[@]}" -gt 0 ]; then
    echo
    echo "rules:"
    echo "  avoid:"
    for e in "${excludes[@]}"; do
      # Single-quote the YAML value; escape any single quotes inside it.
      esc="${e//\'/\'\'}"
      # description is required by Shannon's config-schema.json (rule defn).
      echo "    - type: code_path"
      printf "      value: '%s'\n" "$esc"
      printf "      description: 'Plugin-managed exclude: %s'\n" "$esc"
    done
  fi
} > "$tmp"

# Atomic move; mode 0644 — config is not secret.
mkdir -p "$(dirname "$out")"
mv "$tmp" "$out"
chmod 644 "$out"

echo "OK: wrote $out (excludes=${#excludes[@]}, exploit=$exploit)"
exit 0
