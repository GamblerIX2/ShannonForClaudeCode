#!/usr/bin/env bash
# ensure-target-repo.sh — guarantee that <repo-path> is a git repository.
#
# Shannon's own preflight requires the target source tree to be a git checkout
# (it uses git for baseline tracking and snapshot diffs). When the user points
# Shannon at a plain directory (e.g. a deployed web root that was never under
# version control), we transparently create a *local* baseline: `git init` +
# one initial commit. No remote is ever added.
#
# Usage:
#   ensure-target-repo.sh <repo-path>
#
# Exit codes:
#   0  — already a git repo, OR successfully initialized one
#   1  — argument missing, path invalid, git unavailable, or init failed
#
# Notes:
#   * If <repo-path>/.gitignore is absent, we drop in a minimal one that
#     excludes Shannon's own working directories so the baseline commit
#     doesn't accidentally snapshot scan state. An existing .gitignore is
#     left untouched.
#   * `safe.directory` is set inline so the script works even when the
#     working tree is owned by a different user than the invoker (e.g.
#     root running on a tree owned by www/shannon/etc.).
#   * The baseline commit is authored as `shannon-baseline <shannon@local>`
#     so it's obvious in `git log` where it came from.

set -euo pipefail

REPO_PATH="${1:-}"

if [ -z "$REPO_PATH" ]; then
  echo "Usage: $0 <repo-path>" >&2
  exit 1
fi

case "$REPO_PATH" in
  /*) ABS_REPO="$REPO_PATH" ;;
  *)  ABS_REPO="$(cd "$REPO_PATH" 2>/dev/null && pwd)" ;;
esac

if [ -z "${ABS_REPO:-}" ] || [ ! -d "$ABS_REPO" ]; then
  echo "ERROR: repo path '$REPO_PATH' does not exist or is not a directory." >&2
  exit 1
fi

if ! command -v git >/dev/null 2>&1; then
  echo "ERROR: git is not installed; cannot initialize repo. Install git first." >&2
  exit 1
fi

# Already a git checkout? Done. (.git can be a dir for normal repos, or a
# file for worktrees / submodules — accept both.)
if [ -d "$ABS_REPO/.git" ] || [ -f "$ABS_REPO/.git" ]; then
  echo "ensure-target-repo: $ABS_REPO is already a git repository — nothing to do."
  exit 0
fi

echo "ensure-target-repo: '$ABS_REPO' is not a git repository."
echo "                    auto-initializing a LOCAL baseline (no remote will be added)."

# Defuse "dubious ownership" when invoker != tree owner. Applied to the
# whole tree below this path. Scoped via -c so we don't touch global config.
GIT_C=(-c "safe.directory=$ABS_REPO" -c "safe.directory=*"
       -c "user.email=shannon@local" -c "user.name=shannon-baseline"
       -c "init.defaultBranch=main"
       -c "commit.gpgsign=false" -c "tag.gpgsign=false")

# Defensive .gitignore: only created if the user hasn't already provided one.
# Excludes Shannon's own working dirs so the baseline doesn't snapshot scan
# state, transient build artefacts, or known-secret files.
if [ ! -e "$ABS_REPO/.gitignore" ]; then
  cat > "$ABS_REPO/.gitignore" <<'GITIGNORE'
# Auto-generated by ShannonForClaudeCode (ensure-target-repo.sh).
# Safe to edit or remove if you adopt your own .gitignore.

# Shannon plugin working directories
shannon/
shannon-reports/
.shannon/

# Common secret / build artefacts
.env
.env.local
node_modules/
GITIGNORE
  echo "ensure-target-repo: wrote a minimal .gitignore (Shannon work dirs + .env + node_modules)."
fi

cd "$ABS_REPO"

git "${GIT_C[@]}" init -q

# Stage everything. If staging hits an exotic permission/file-mode issue we
# still want the init to succeed — the --allow-empty commit below covers it.
git "${GIT_C[@]}" add -A 2>/dev/null || \
  echo "ensure-target-repo: warning: 'git add -A' returned non-zero; falling back to empty commit." >&2

git "${GIT_C[@]}" commit --allow-empty -q \
  -m "shannon baseline (auto-created by ShannonForClaudeCode; local only, no remote)"

echo "ensure-target-repo: initialized git repo at $ABS_REPO"
git "${GIT_C[@]}" log -1 --format='                    HEAD = %h %s'
exit 0
